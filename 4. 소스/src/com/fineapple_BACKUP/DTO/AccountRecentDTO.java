package com.fineapple.DTO;

public class AccountRecentDTO {

	private String seq;
	private String name;
	private String icount;
	private String iamount;
	private String ocount;
	private String oamount;
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIcount() {
		return icount;
	}
	public void setIcount(String icount) {
		this.icount = icount;
	}
	public String getIamount() {
		return iamount;
	}
	public void setIamount(String iamount) {
		this.iamount = iamount;
	}
	public String getOcount() {
		return ocount;
	}
	public void setOcount(String ocount) {
		this.ocount = ocount;
	}
	public String getOamount() {
		return oamount;
	}
	public void setOamount(String oamount) {
		this.oamount = oamount;
	}
}
